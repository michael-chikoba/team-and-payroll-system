import axios from 'axios'
import LandingNavbar from '@/components/landing/LandingNavbar.vue'
import LandingFooter from '@/components/landing/LandingFooter.vue'

export default {
  name: 'ContactView',
  components: { LandingNavbar, LandingFooter },
  data() {
    return {
      contactForm: { name: '', email: '', subject: '', message: '' },
      contactSubmitting: false,
      contactFormError: null,
      contactSuccessMessage: null,
      demoForm: { company_name: '', first_name: '', last_name: '', email: '', phone: '', employee_count: '', message: '' },
      demoSubmitting: false,
      demoFormError: null,
      demoSuccessMessage: null,
      toast: { show: false, message: '', type: 'success' },
    }
  },
  methods: {
    startLiveChat() {
      if (window.ChatSystem && typeof window.ChatSystem.open === 'function') window.ChatSystem.open()
    },
    showToast(message, type = 'success') {
      this.toast = { show: true, message, type }
      setTimeout(() => { this.toast.show = false }, 3000)
    },
    handleApiError(err) {
      let errorMsg = 'An unexpected error occurred.'
      if (err.code === 'ERR_NETWORK') {
        errorMsg = 'Network error. Please check your connection.'
      } else if (err.response?.status === 422) {
        const responseData = err.response.data
        if (responseData.errors) {
          errorMsg = Object.values(responseData.errors).flat().join('. ')
        } else if (responseData.message) {
          errorMsg = responseData.message
        } else {
          errorMsg = 'Please check the form for errors.'
        }
      } else if (err.response?.status === 429) {
        errorMsg = 'Too many requests. Please wait a moment and try again.'
      } else if (err.response?.status === 500) {
        errorMsg = 'Server error. Please try again later or contact support.'
      } else {
        errorMsg = err.response?.data?.message || errorMsg
      }
      console.error('API Error Details:', { status: err.response?.status, data: err.response?.data, message: err.message })
      return errorMsg
    },
    validateContactForm() {
      this.contactFormError = null
      if (!this.contactForm.name.trim())    { this.contactFormError = 'Please enter your name.'; return false }
      if (!this.contactForm.email.trim())   { this.contactFormError = 'Please enter your email address.'; return false }
      if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(this.contactForm.email)) { this.contactFormError = 'Please enter a valid email address.'; return false }
      if (!this.contactForm.subject.trim()) { this.contactFormError = 'Please enter a subject.'; return false }
      if (!this.contactForm.message.trim()) { this.contactFormError = 'Please enter your message.'; return false }
      return true
    },
    async submitContactForm() {
      if (!this.validateContactForm()) return
      this.contactSubmitting = true
      this.contactFormError = null
      try {
        const payload = new FormData()
        payload.append('name', this.contactForm.name)
        payload.append('email', this.contactForm.email)
        payload.append('subject', this.contactForm.subject)
        payload.append('message', this.contactForm.message)
        const response = await axios.post('/api/contact', payload, { headers: { 'Content-Type': 'multipart/form-data', 'Accept': 'application/json' }, timeout: 10000 })
        this.contactSuccessMessage = response.data.message || "Message sent! We'll get back to you soon."
        this.showToast('Message sent successfully!', 'success')
      } catch (err) {
        console.error('Contact form error:', err)
        this.contactFormError = this.handleApiError(err)
      } finally {
        this.contactSubmitting = false
      }
    },
    validateDemoForm() {
      this.demoFormError = null
      if (!this.demoForm.company_name.trim()) { this.demoFormError = 'Please enter your company name.'; return false }
      if (!this.demoForm.first_name.trim())   { this.demoFormError = 'Please enter your first name.'; return false }
      if (!this.demoForm.last_name.trim())    { this.demoFormError = 'Please enter your last name.'; return false }
      if (!this.demoForm.email.trim())        { this.demoFormError = 'Please enter your email address.'; return false }
      if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(this.demoForm.email)) { this.demoFormError = 'Please enter a valid email address.'; return false }
      if (!this.demoForm.phone.trim())        { this.demoFormError = 'Please enter your phone number.'; return false }
      return true
    },
    async submitDemoRequest() {
      if (!this.validateDemoForm()) return
      this.demoSubmitting = true
      this.demoFormError = null
      try {
        const payload = new FormData()
        payload.append('company_name', this.demoForm.company_name)
        payload.append('first_name',   this.demoForm.first_name)
        payload.append('last_name',    this.demoForm.last_name)
        payload.append('email',        this.demoForm.email)
        payload.append('phone',        this.demoForm.phone)
        if (this.demoForm.employee_count) payload.append('employee_count', this.demoForm.employee_count)
        if (this.demoForm.message.trim()) payload.append('message', this.demoForm.message)
        const response = await axios.post('/api/demo-requests', payload, { headers: { 'Content-Type': 'multipart/form-data', 'Accept': 'application/json' }, timeout: 10000 })
        this.demoSuccessMessage = response.data.message || "Thank you! We'll contact you shortly to schedule your demo."
        this.showToast('Demo request submitted successfully!', 'success')
      } catch (err) {
        console.error('Demo request error:', err)
        this.demoFormError = this.handleApiError(err)
      } finally {
        this.demoSubmitting = false
      }
    },
    resetDemoForm() {
      this.demoForm = { company_name: '', first_name: '', last_name: '', email: '', phone: '', employee_count: '', message: '' }
      this.demoFormError = null
      this.demoSuccessMessage = null
      this.demoSubmitting = false
    },
  },
}
</script>

